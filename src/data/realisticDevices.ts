export const REALISTIC_DEVICE_GROUPS = REALISTIC_DEVICES.reduce((groups, device) => {
  const baseName = device.name;
  if (!groups[baseName]) {
    groups[baseName] = {
      name: device.name,
      variants: [],
    };
  }
  groups[baseName].variants.push(device);
  return groups;
}, {} as Record<string, { name: string; variants: RealisticDeviceConfig[] }>);

export const getRealisticDeviceById = (id: string) => {
  return REALISTIC_DEVICES.find(d => d.id === id);
};